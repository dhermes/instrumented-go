package indirect

// IdentityInt is the identity function for integers.
func IdentityInt(i int) int {
	return i
}

// IdentityInt64 is the identity function for 64 bit integers.
func IdentityInt64(i int64) int64 {
	return i
}

// IdentityFloat32 is the identity function for 32 bit floating point numbers.
func IdentityFloat32(f float32) float32 {
	return f
}

// IdentityFloat64 is the identity function for 64 bit floating point numbers.
func IdentityFloat64(f float64) float64 {
	return f
}
